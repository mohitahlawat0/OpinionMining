    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con=null;
        String comment=jTextArea1.getText().trim();
        if(comment.equals("") || comment=="" || comment.equals(null))
            JOptionPane.showMessageDialog(this, "Please Enter Comment First");
        else{
        float review=machinelearningimpl.findReview(comment);
        String query="insert into ReviewTable (commenter,commenter_id,comment,review,img_id) values(?,?,?,?,?)";
        PreparedStatement ps=null;
        if(con==null)
            con=Createconnection.Connect();
        try{
            ps=con.prepareStatement(query);
            ps.setString(1,OpinionMining.name);
            ps.setInt(2, OpinionMining.login_id);
            ps.setString(3, comment);
            ps.setFloat(4, review);
            ps.setString(5, img_id);
            int i=0;
            i=ps.executeUpdate();
            if(i>0)
                JOptionPane.showMessageDialog(this,"Thanks for Your comment");
            jTextArea1.setText("");
            ps.close();
            con.close();
            populateJTable();
        }catch(SQLException e){System.out.println(e);}
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed


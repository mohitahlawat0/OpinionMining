    public void populateJTable(){
          ArrayList<Commentbean> list=machinelearningimpl.getcomment();
          //System.out.println(list.size());
          String[] columnname={"Commenter","Comment","Review Out of 5"};
          Object[][] rows=new Object[list.size()][3];
          for(int i=0;i<list.size();i++){
              rows[i][0]=list.get(i).getCommenter();
              rows[i][1]=list.get(i).getComment();
              rows[i][2]=list.get(i).getReview();
          }
          TheModel2 model= new TheModel2(rows,columnname);
          DefaultTableCellRenderer centerrender=new DefaultTableCellHeaderRenderer();
          jTable1.setModel(model);
          jTable1.setRowHeight(40);
          jTable1.getColumnModel().getColumn(0).setCellRenderer(centerrender);
          jTable1.getColumnModel().getColumn(1).setCellRenderer(centerrender);
          //jTable1.getColumnModel().getColumn(1).setCellRenderer(centerrender);
          jTable1.getColumnModel().getColumn(2).setCellRenderer(centerrender);
          
          //jTable1.getColumnModel().getColumn(4).setPreferredWidth(150);
      }
    public void getImage(){
        Connection con=null;
        String query="select image from imgtbl where img_id=?";
        PreparedStatement ps=null;
        ResultSet rs=null;
        if(con==null)
            con=Createconnection.Connect();
        try{
            ps=con.prepareStatement(query);
            ps.setString(1, img_id);
            rs=ps.executeQuery();
            while(rs.next()){
                byte[] b=rs.getBytes(1);
                jLabel2.setIcon(new ImageIcon(new ImageIcon(b).getImage().getScaledInstance(430,335 , Image.SCALE_SMOOTH)));
            }
            ps.close();
            rs.close();
            con.close();
        }catch(SQLException e){System.out.println(e);}
    }

      public void populateJTable(){
          ArrayList<Imagebean> list=machinelearningimp.BindData();
          String[] columnname={"Post Number","Uploader","Image","Total Review Out of 5"};
          Object[][] rows=new Object[list.size()][4];
          for(int i=0;i<list.size();i++){
              rows[i][0]=list.get(i).getImgid();
              if(list.get(i).getUploader()!=null)
              rows[i][1]=list.get(i).getUploader();
              if(list.get(i).getMyImage()!= null){
                  ImageIcon image=new ImageIcon(new ImageIcon(list.get(i)
                           .getMyImage()).getImage().getScaledInstance(150,120, Image.SCALE_SMOOTH));
                  rows[i][2]=image;
                  //System.out.println(rows[i][1]);
              }
              else{
                 
                  rows[i][2]=null;
                  
              }
              rows[i][3]=String.format("%.1f",machinelearningimp.calculateReview(list.get(i).getImgid()));
              
          }
          TheModel model= new TheModel(rows,columnname);
          DefaultTableCellRenderer centerrender=new DefaultTableCellHeaderRenderer();
          jTable1.setModel(model);
          jTable1.setRowHeight(120);
          jTable1.getColumnModel().getColumn(0).setCellRenderer(centerrender);
          jTable1.getColumnModel().getColumn(1).setCellRenderer(centerrender);
          //jTable1.getColumnModel().getColumn(1).setCellRenderer(centerrender);
          jTable1.getColumnModel().getColumn(3).setCellRenderer(centerrender);
          Font font = new Font("Ariel", Font.BOLD,48);
		
		//set font for JLabel
		jTable1.setFont(font);
          //jTable1.getColumnModel().getColumn(4).setPreferredWidth(150);
          //DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
    //renderer.setHorizontalAlignment(SwingConstants.RIGHT);
    //jTable1.getColumn(1).setHeaderRenderer(renderer);
      }

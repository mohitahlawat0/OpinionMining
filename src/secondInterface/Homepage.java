    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jFileChooser1=new JFileChooser();
        jFileChooser1.setVisible(true);
        jFileChooser1.setMultiSelectionEnabled(false);
        jFileChooser1.showOpenDialog(this);
        File file=jFileChooser1.getSelectedFile();
        if(file!=null)
            filepath=file.getPath();
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/opinionmining","root","mysql@123456");
            PreparedStatement ps=null;
            ps=con.prepareStatement("insert into imgtbl (img_name,image,uploader,uploader_id)values(?,?,?,?)");  
ps.setString(1,filepath);
            FileInputStream fin=new FileInputStream(file);  
ps.setBinaryStream(2,fin,fin.available());  
ps.setString(3, OpinionMining.name);
ps.setInt(4, OpinionMining.login_id);
int i=ps.executeUpdate();  
if(i>0)
    JOptionPane.showMessageDialog(this, "Image uploaded Successfully");
        ps.close();
con.close();  
populateJTable();
        }catch(ClassNotFoundException | SQLException |IOException  e){JOptionPane.showMessageDialog(this, e);}
    }//GEN-LAST:event_jButton1ActionPerformed

